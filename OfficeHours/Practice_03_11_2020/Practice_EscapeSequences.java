package OfficeHours.Practice_03_11_2020;

public class Practice_EscapeSequences {
    public static void main(String[] args) {
        System.out.println("    Java is programming language");
        System.out.println("\tJava is programming language");
            // \ it is used for giving paragraph space
        System.out.println("\nWe love learning Java");
        // \ it is used for starting a new line

        System.out.println("\n\tWe love learning Java");

        System.out.println("I love the TV show \"The Talking Dead\"");
        System.out.println("My favorite quote is \"Night is Dark, full of terrors\"");
        // \ it is used for printing double quote
        System.out.println("/ \\");
        // \\ is used for printing single back slash

        System.out.println( '7' + (3+9));



    }


}
